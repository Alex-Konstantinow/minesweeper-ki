package de.unima.info.ki.minesweeper.api;

public class Tupel {
    private int x;
    private int y;

    public Tupel(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }
}
